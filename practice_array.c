#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>
#include<stdlib.h>


int main() {
	int n;
	scanf("%d", &n);
	char* str = (char*)malloc((n + 1) * sizeof(char));
	scanf("%s", str);
	char a[10][5][3];
	int i, j, k;
	for (i = 0;i < 10;i++) {
		for (j = 0;j < 5;j++) {
			for (k = 0;k < 3;k++) {
				a[i][j][k] = 'X';
			}
		}
	}
	for (j = 1;j <= 3;j++) {
		a[0][j][1] = '.';
	}
	for (j = 0;j <= 4;j++) {
		a[1][j][0] = '.';
		a[1][j][1] = '.';
	}
	for (j = 1;j <= 4;j++) {
		a[7][j][0] = '.';
		a[7][j][1] = '.';
	}
	a[2][1][0] = '.', a[2][1][1] = '.', a[2][3][1] = '.', a[2][3][2] = '.';
	a[3][1][0] = '.', a[3][1][1] = '.', a[3][3][0] = '.', a[3][3][1] = '.';
	a[4][0][1] = '.', a[4][1][1] = '.', a[4][3][0] = '.', a[4][3][1] = '.', a[4][4][0] = '.', a[4][4][1] = '.';
	a[5][1][1] = '.', a[5][1][2] = '.', a[5][3][0] = '.', a[5][3][1] = '.';
	a[6][1][1] = '.', a[6][1][2] = '.', a[6][3][1] = '.';
	a[8][1][1] = '.', a[8][3][1] = '.';
	a[9][1][1] = '.', a[9][3][0] = '.', a[9][3][1] = '.';

	int t;
	for (j = 0;j < 5;j++) {
		for (t = 0;t < n;t++) {
			int num = str[t] - '0';
			for (int k = 0; k < 3; k++) {
				printf("%c", a[num][j][k]);
			}
			if (t != n - 1) {
				printf(".");
			}
		}
		printf("\n");
	}
	return 0;
}